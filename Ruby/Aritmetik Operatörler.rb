a = 6
b = 2

# + : Toplama Operatörü
puts a + b
puts "merhaba" + "ali"

# - : Çıkarma Operatörü
puts a - b

# * : Çarpma Operatörü
puts a * b

# / : Bölme Operatörü
puts a / b
puts 2 / 2.0

# .div() : Tamsayı Bölme Operatörü
puts 5.div(2)

# .fdiv() : Ondalık Sayı Bölme Operatörü
puts 5.fdiv(2)

# ** : Üs Alma Operatörü
puts a**2

# % : Mod Alma Operatörü
puts a % b

# .modulo() : Mod Alma
puts a.modulo(b)

# .remainder() : Bölme İşleminden Kalan
puts 3.remainder(2)

# .divmod() : Bölüm ve Kalanı Array olarak verir.
puts "[ Bölüm, Kalan ] == #{6.divmod(8)} "

# .quo() : Bölümü verir
puts 6.quo(2)